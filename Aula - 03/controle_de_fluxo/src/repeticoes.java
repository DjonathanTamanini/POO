public class repeticoes {
    public static void main(String[] args) {
        int x = 0; // criar a variavel antes de iniciar o while
        while (x < 10){
            System.out.print(x+" ");
            x++;
        }
        System.out.println("\n-------------");
        int i = 20;
        do {
            System.out.print(i+" ");
            i++;
        }while (i < 30);

        System.out.println("\n-------------");
        for (int j = 10; j = 40; j++);{ //for pode criar variavel dentro do loop
            System.out.print(j+" ");
        }


    }
}
