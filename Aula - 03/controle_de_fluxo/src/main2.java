public class main2 {
    public static void main(String[] args) {
        int x = 10;
        // switch - case
        // escolha - caso
        switch (x){
            case 0:
                System.out.println("é zero");
                break;
            case 1:
                System.out.println("é um");
                break;
            default: // X != de todos os anteriores
                System.out.println("Não sei o valor");
        }
    }
}
